{
	DDRC = 0xF0; PORTC = 0x0F;
	DDRA = 0xFF; PORTA = 0x00;
	DDRB = 0xFF; PORTB = 0x00;
	DDRD = 0xFF; PORTD = 0x00;
	

	static task task1;

	task *tasks[] = {&task1};

	unsigned long int Tick_period = 300;
	unsigned long int Tick_tick = 30;
	//const unsigned short numTasks = sizeof(task)/sizeof(task*);

	task1.state = -1;
	task1.period = Tick_period;
	task1.elapsedTime = Tick_tick;
	task1.TickFct = &Tick;

	TimerOn();
	TimerSet(Tick_period);
	LCD_init();
	const char* message = "Congratulations!";
	LCD_DisplayString(1, message);
	LCD_Cursor(position);

	while(1)
	{
		tasks[0]->TickFct(0);
		while(!TimerFlag){}
		TimerFlag=0;
	}
}